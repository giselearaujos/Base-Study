/* JavaScript é uma linguagem dinamicamente tipada.
  Isso significa que você não precisa especificar o tipo de dado quando vai declarar uma variável.
  O tipo de dado da variável é inferido automaticamente a partir do seu valor.
*/

// Declaração de variável

let name; // Declara variável
name = "Giza"; // Inicializa uma variável
console.log(name); // Giza

name = "Lana"; // Reatribuição
console.log(name); // Lana

// Não podemos redeclarar variáveis com let
// let name = "Olíva";
