// => Operadores aritméticos

// Adição (+), concatenar e tenta converter o operando em um número, sempre que possível.
const num1 = 23;
const num2 = 22;
console.log(num1 + num2);

const whatIsThis = "100";
console.log(+whatIsThis);
console.log("--------------------------------");

// Subtração (-)
const salary = 1000;
const discount = 120;
console.log(salary - discount);
console.log("--------------------------------");

// Multiplicação (*)
const fees = 20;
console.log(salary * fees);
console.log("--------------------------------");

// Divisão (/)
const division = 5;
console.log(salary / division);
console.log("--------------------------------");

// Módulo (%)
const rest = 2;
console.log(salary % rest);
console.log("--------------------------------");

// Incremento (++)
let steps = 0;
console.log(++steps);
console.log("--------------------------------");

// Decremento (--)
let points = 10;
console.log(--points);
console.log("--------------------------------");

// Negação (-) Operador unário. Retorna a negação de seu operando.
const negative = 3;
console.log(-negative);
console.log("--------------------------------");

// Exponenciação (**)
const expo = 5;
console.log(salary ** expo);
console.log("--------------------------------");

